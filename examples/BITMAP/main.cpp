void setup()
{
  myOLED.OLEDbegin(OLEDcontrast); // initialize the OLED
  myOLED.OLEDFillScreen(0xFF, 0); // Clears screen
  bcm2835_delay(5000);
}

void myLoop()
{
	Test1(); // Method (1) OLED bitmap method,Any buffer  setting on.
	Test2(); // Method (2) OLED buffer method, MULTI_BUFFER or SINGLE BUFFER setting on.
	Test3(); // Method (3) OLED update mutlibuffer mode  , MULTI_BUFFER setting on.
}


void Test1(void)
{
  // Method (1) OLED bitmap method, Any buffer  setting on 
  //  x ,y,w,h, bitmap
  	myOLED.OLEDBitmap(0, 0 , myOLEDwidth, myOLEDheight, fullscreenBitmap);
	bcm2835_delay(5000);
	myOLED.OLEDFillScreen(0x00, 0); 
}

void Test2(void)
{
	// Method (2) OLED buffer method , MULTI_BUFFER or SINGLE BUFFER setting, on
	//  x ,y,w,h, bitmap
	 myOLED.OLEDBuffer(30, 10, 20, 20, (uint8_t*)smallImage);
	 myOLED.OLEDBuffer(70, 10, 20, 20, (uint8_t*)smallImage);
	 bcm2835_delay(5000);
	myOLED.OLEDFillScreen(0x00, 0); 
}

void Test3(void)
{
	// Method (3) multibuffer mode  , MULTI_BUFFER setting on,
	MultiBuffer Whole_screen;
	Whole_screen.screenbitmap = (uint8_t*) fullscreenBitmap;
	Whole_screen.width = myOLEDwidth;
	Whole_screen.height = 64;
	Whole_screen.xoffset = 0; // offset on display
	Whole_screen.yoffset = 0 ;
	myOLED.ActiveBuffer = &Whole_screen;
	myOLED.OLEDupdate();
	bcm2835_delay(5000);
	myOLED.OLEDFillScreen(0x00, 0); 
}


